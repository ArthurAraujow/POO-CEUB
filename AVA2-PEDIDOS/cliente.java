public class cliente {
    private String nome;
    private int cpf;
    private String endereco;
    private String cep;
    private String cidade;
    private int telefone;
    public cliente(String nome, int cpf, String endereco, String cep, String cidade, int telefone) {
        this.nome = nome;
        this.cpf = cpf;
        this.endereco = endereco;
        this.cep = cep;
        this.cidade = cidade;
        this.telefone = telefone;
    }

}